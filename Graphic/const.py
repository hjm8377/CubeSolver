def color_to_rgb(n):
    if n is 'g':
        return 0.0, 0.7, 0.2
    elif n is 'r':
        return 0.8, 0.118, 0.118
    elif n is 'b':
        return 0.0, 0.318, 0.729
    elif n is 'o':
        return 1.0, 0.345, 0.0
    elif n is 'y':
        return 1.0, 0.85, 0.1
    elif n is 'w':
        return 1.0, 1.0, 1.0

